import { cp, mkdir, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildOpenIconCatalog } from '../../../packages/open-icon-svg/scripts/openIconCatalog.mjs';
import { buildOpenIconSiteData } from '../../../packages/open-icon-svg/scripts/openIconSiteData.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const appRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(appRoot, '../..');
const iconsRoot = path.join(repoRoot, 'packages', 'open-icon-svg', 'icons');
const generatedDir = path.join(appRoot, 'src', 'generated');
const generatedFile = path.join(generatedDir, 'openIconApi.generated.ts');
const assetsIconsDir = path.join(appRoot, 'assets', 'icons');

const toConst = (value) => JSON.stringify(value, null, 2);

const renderGeneratedModule = ({ catalog, siteData }) => `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/build-api-assets.mjs

export const OPEN_ICON_API_CATEGORIES = ${toConst(catalog.categories)} as const;
export const OPEN_ICON_API_NAME_TO_FILE = ${toConst(catalog.nameToFile)} as const;
export const OPEN_ICON_API_ALIAS_TO_NAME = ${toConst(catalog.aliasToName)} as const;
export const OPEN_ICON_API_CATEGORY_TO_NAMES = ${toConst(catalog.categoryToNames)} as const;
export const OPEN_ICON_API_ICONS = ${toConst(siteData.icons.entries)} as const;
export const OPEN_ICON_API_PACKAGES = ${toConst(siteData.packages.entries)} as const;
`;

export const buildApiAssets = async () => {
	const catalog = await buildOpenIconCatalog({ iconsRoot });
	const siteData = await buildOpenIconSiteData({ apiBaseUrl: 'https://api.open-icon.org' });

	await mkdir(generatedDir, { recursive: true });
	await writeFile(generatedFile, `${renderGeneratedModule({ catalog, siteData })}\n`, 'utf8');

	await rm(assetsIconsDir, { recursive: true, force: true });
	await mkdir(path.dirname(assetsIconsDir), { recursive: true });
	await cp(iconsRoot, assetsIconsDir, { recursive: true });

	console.log(`[open-icon-api] Wrote ${generatedFile}`);
	console.log(`[open-icon-api] Copied icons to ${assetsIconsDir}`);
};

if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
	buildApiAssets().catch((error) => {
		console.error('[open-icon-api] Failed to build API assets.');
		console.error(error);
		process.exit(1);
	});
}
