import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
	buildOpenIconCatalog,
	renderOpenIconCatalogTs,
} from '../../open-icon-svg/scripts/openIconCatalog.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const packageRoot = path.resolve(__dirname, '..');
const iconsRoot = path.resolve(packageRoot, '../open-icon-svg/icons');
const generatedRoot = path.join(packageRoot, 'src', 'generated');
const srcRoot = path.join(packageRoot, 'src');

const quote = (value) => JSON.stringify(value);

const toPascalCase = (value) =>
	value
		.split(/[^a-zA-Z0-9]+/g)
		.filter(Boolean)
		.map((segment) => segment.charAt(0).toUpperCase() + segment.slice(1))
		.join('');

const buildIconExports = (entries) => {
	const baseNameCounts = new Map();
	for (const entry of entries) {
		const baseName = entry.iconName.split('/').pop() ?? entry.iconName;
		baseNameCounts.set(baseName, (baseNameCounts.get(baseName) ?? 0) + 1);
	}

	const exportAssignments = [];
	const iconMapEntries = [];
	const iconExportNames = [];

	for (const entry of entries) {
		const [category, baseName = entry.iconName] = entry.iconName.split('/');
		const baseExportName = `Icon${toPascalCase(baseName) || 'Unnamed'}`;
		const prefixedExportName = `Icon${toPascalCase(category)}${toPascalCase(baseName) || 'Unnamed'}`;
		const svgValue = quote(entry.svg);

		if (baseNameCounts.get(baseName) === 1) {
			exportAssignments.push(`export const ${baseExportName} = ${svgValue} as const;`);
			exportAssignments.push(`export const ${prefixedExportName} = ${baseExportName};`);
			iconMapEntries.push(`\t${quote(entry.iconName)}: ${baseExportName},`);
			iconExportNames.push(baseExportName, prefixedExportName);
			continue;
		}

		exportAssignments.push(`export const ${prefixedExportName} = ${svgValue} as const;`);
		iconMapEntries.push(`\t${quote(entry.iconName)}: ${prefixedExportName},`);
		iconExportNames.push(prefixedExportName);
	}

	return {
		assignments: exportAssignments.join('\n'),
		iconMapEntries: iconMapEntries.join('\n'),
		iconExportNames: Array.from(new Set(iconExportNames)).sort((left, right) => left.localeCompare(right)),
	};
};

const renderIconExportsTs = (entries) => {
	const { assignments, iconMapEntries } = buildIconExports(entries);

	return `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/generate-open-icon-package.mjs

import type { OpenIconName } from './openIconCatalog.generated.js';

${assignments}

export const OPEN_ICON_SVG_BY_NAME: Record<OpenIconName, string> = {
${iconMapEntries}
};
`;
};

const renderIconsEntryTs = (entries) => {
	const { iconExportNames } = buildIconExports(entries);

	return `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/generate-open-icon-package.mjs

export {
\t${iconExportNames.join(',\n\t')},
} from './generated/openIconIcons.generated.js';
`;
};

const readSvg = async (filePath) => fs.readFile(path.join(iconsRoot, filePath.replace(/^icons\//, '')), 'utf8');

const main = async () => {
	await fs.mkdir(generatedRoot, { recursive: true });

	const catalog = await buildOpenIconCatalog({
		iconsRoot,
		filePathPrefix: 'icons',
	});

	const entriesWithSvg = await Promise.all(
		catalog.entries.map(async (entry) => ({
			...entry,
			svg: await readSvg(entry.filePath),
		}))
	);

	await Promise.all([
		fs.writeFile(
			path.join(generatedRoot, 'openIconCatalog.generated.ts'),
			renderOpenIconCatalogTs(catalog),
			'utf8'
		),
		fs.writeFile(
			path.join(generatedRoot, 'openIconIcons.generated.ts'),
			renderIconExportsTs(entriesWithSvg),
			'utf8'
		),
		fs.writeFile(path.join(srcRoot, 'icons.ts'), renderIconsEntryTs(entriesWithSvg), 'utf8'),
	]);

	console.log(`[open-icon] Generated catalog and icon exports for ${entriesWithSvg.length} icons`);
};

await main();
